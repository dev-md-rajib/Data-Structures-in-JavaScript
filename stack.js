/**
 * Js doesn't have built in stack
 * Instead the array contains methods that can used to operate LIFO operations
 * Array has push() and pop() methods/functions, these can be used to the array
 * as a stack.
 */

let stack = [];
//push
stack.push(1);

//accessing top element
stack[stack.length - 1];

//pop
stack.pop();
